package NivelBasico;

public class RedeclararValor {
    public static void main(String[] args) {


        //Redeclarar valor
        String nomeNinja = "Naruto uzumaki";
        nomeNinja = "Boruto uzumaki";  // Naruto uzumaki --> Boruto uzumaki
        System.out.println(nomeNinja);


        int idade = 10;
        idade = 18;                    // 10 --> 18
        System.out.println(idade);


    }
}
